def sumar(op1,op2):
    """Esta funcion lo que hace es sumar"""
    print("El resultado de tu suma es: ",op1+op2)

def restar(op1,op2):
    """Esta funcion lo que hace es restar"""
    print("El resultado de la resta es: ",op1-op2)

def dividir(op1,op2):
    """Esta funcion lo que hace es dividir"""
    print("El resultado de la division es: ",op1/op2)

